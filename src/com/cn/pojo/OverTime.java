package com.cn.pojo;

import java.sql.Date;

public class OverTime {
	//编号
	private int overtime_id;
	//加班员工编号
	private int overtime_user_id;
	//加班开始时间
	private Date overtime_begin_time;
	//加班结束时间
	private Date overtime_end_time;
	//所属部门
	private int overtime_department_id;
	//加班申请状态
	private int overtime_state;
	//员工表
	private User user;
	//申请状态
	private State state;
	//部门
	private Department department;
	
	
	public int getOvertime_state() {
		return overtime_state;
	}
	public void setOvertime_state(int overtime_state) {
		this.overtime_state = overtime_state;
	}
	public int getOvertime_department_id() {
		return overtime_department_id;
	}
	public void setOvertime_department_id(int overtime_department_id) {
		this.overtime_department_id = overtime_department_id;
	}
	public Department getDepartment() {
		return department;
	}
	public void setDepartment(Department department) {
		this.department = department;
	}
	public State getState() {
		return state;
	}
	public void setState(State state) {
		this.state = state;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public int getOvertime_id() {
		return overtime_id;
	}
	public void setOvertime_id(int overtime_id) {
		this.overtime_id = overtime_id;
	}
	public int getOvertime_user_id() {
		return overtime_user_id;
	}
	public void setOvertime_user_id(int overtime_user_id) {
		this.overtime_user_id = overtime_user_id;
	}
	public Date getOvertime_begin_time() {
		return overtime_begin_time;
	}
	public void setOvertime_begin_time(Date overtime_begin_time) {
		this.overtime_begin_time = overtime_begin_time;
	}
	public Date getOvertime_end_time() {
		return overtime_end_time;
	}
	public void setOvertime_end_time(Date overtime_end_time) {
		this.overtime_end_time = overtime_end_time;
	}
}
