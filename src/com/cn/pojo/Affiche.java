package com.cn.pojo;

import java.sql.Date;

public class Affiche {
	//公告编号
	private int affiche_id;
	//公告内容
	private String affiche_name;
	//创建公告时间
	private Date affiche_time;
	//创建公告人的编号
	private int affiche_user_id;
	//用户表
	private User user;
	
	public int getAffiche_id() {
		return affiche_id;
	}
	public void setAffiche_id(int affiche_id) {
		this.affiche_id = affiche_id;
	}
	public String getAffiche_name() {
		return affiche_name;
	}
	public void setAffiche_name(String affiche_name) {
		this.affiche_name = affiche_name;
	}
	public Date getAffiche_time() {
		return affiche_time;
	}
	public void setAffiche_time(Date affiche_time) {
		this.affiche_time = affiche_time;
	}
	public int getAffiche_user_id() {
		return affiche_user_id;
	}
	public void setAffiche_user_id(int affiche_user_id) {
		this.affiche_user_id = affiche_user_id;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
}
