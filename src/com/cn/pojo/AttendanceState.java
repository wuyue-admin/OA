package com.cn.pojo;

public class AttendanceState {
	//考勤打卡状态编号
	private int attendance_state_id;
	//考勤打卡状态名称
	private String attendance_state_name;
	
	public int getAttendance_state_id() {
		return attendance_state_id;
	}
	public void setAttendance_state_id(int attendance_state_id) {
		this.attendance_state_id = attendance_state_id;
	}
	public String getAttendance_state_name() {
		return attendance_state_name;
	}
	public void setAttendance_state_name(String attendance_state_name) {
		this.attendance_state_name = attendance_state_name;
	}
}
